public class Ejemplo_strings
{
	public static void main(String args[]) {
		        
		String curso = "Estructura de Datos y Algoritmos";

		System.out.println("La cadena es: '"+curso+"'");
		System.out.println("Tamaño: "+curso.length());
		System.out.println("En mayuscula: "+curso.toUpperCase());
		System.out.println("En minuscula: "+curso.toLowerCase());

	}
}
