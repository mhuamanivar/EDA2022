
public class Operators {
	public static void main(String[] args) {
		
		
		int x=20;
		
		System.out.println("x inicial:   " + x);
		//suma
		x+=3;
		System.out.println("operador +=3  " + x);
		
		//resta
		x-=1;
		System.out.println("operador -=1  " + x);
		
		//multiplicacion
		x*=2;
		System.out.println("operador *=2  " + x);
		
		//division
		x/=3;
		System.out.println("operador /=3  " + x);
		
		//modulo
		x%=4;
		System.out.println("operador %=4  " + x);
		
		//potencia
		x^=3;
		System.out.println("operador ^=3  " + x);
		
	}
}
